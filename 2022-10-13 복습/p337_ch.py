#문자열 내장 함수
print(chr(97))                          #chr() : 정수를 문자로 변환
print(ord("a"))                         #ord() : 문자를 정수로 변환, 코드값 반환. 문자가 알파벳이 아니라면 유니코드 값을 반환
print(len("Python is powerful!"))       #len() : 문자열의 길이를 반환
print(str(1+2j))                        #str() : 객체의 문자열 표현을 반환

